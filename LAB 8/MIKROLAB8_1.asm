STAK    SEGMENT PARA STACK 'STACK'
        DW 20 DUP(?)
STAK    ENDS

DATA    SEGMENT PARA 'DATA'
        DIGITS  DB 00H 
DATA    ENDS

CODE    SEGMENT PARA 'CODE'
        ASSUME CS:CODE, DS:DATA, SS:STAK
START PROC
	MOV AX, DATA
	MOV DS, AX
	
	mov DX, 062H  
	MOV AL, 00H

INCREMENT:	
	OUT DX, AL
	CALL DELAY
	INC AL
	CMP AL, 0FFH
        JNZ INCREMENT
DECREMENT:
	OUT DX, AL
	CALL DELAY
	DEC AL
	CMP AL, 00H
	JNZ DECREMENT
	JMP INCREMENT
	RET
START ENDP
	
DELAY PROC NEAR
	PUSH CX
	MOV CX, 0003H ;
	L1:
	LOOP L1
	POP CX
	RET
DELAY ENDP	
	
CODE    ENDS
        END START